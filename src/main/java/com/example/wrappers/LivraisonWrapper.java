package com.example.wrappers;

public class LivraisonWrapper {
    private String adresseDepart;
    private String adresseDestination;
    private String detailsColis;

    // Getters et Setters
    public String getAdresseDepart() {
        return adresseDepart;
    }

    public void setAdresseDepart(String adresseDepart) {
        this.adresseDepart = adresseDepart;
    }

    public String getAdresseDestination() {
        return adresseDestination;
    }

    public void setAdresseDestination(String adresseDestination) {
        this.adresseDestination = adresseDestination;
    }

    public String getDetailsColis() {
        return detailsColis;
    }

    public void setDetailsColis(String detailsColis) {
        this.detailsColis = detailsColis;
    }
}